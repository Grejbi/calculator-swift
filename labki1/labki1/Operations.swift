import Foundation

struct Operations {
    func Addition(v1:Int, v2:Int) -> Int
    {
        return v1 + v2
        
    }
    func Substraction(v1:Int, v2:Int) -> Int
    {
        return v1 - v2
        
    }
    func Multiplication(v1:Int, v2:Int) -> Int
    {
        return v1 * v2
        
    }
    func Division(v1:Int, v2:Int) -> Int
    {
        return v1 / v2
        
    }
    func Sinus(v1:Float) -> Float
    {
        return sin(v1)
        
    }
    
}
